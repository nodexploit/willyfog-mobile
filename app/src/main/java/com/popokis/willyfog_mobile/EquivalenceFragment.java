package com.popokis.willyfog_mobile;

import android.app.ProgressDialog;
import android.content.Context;
import android.os.Bundle;
import android.support.v4.app.Fragment;
import android.support.v7.widget.GridLayoutManager;
import android.support.v7.widget.LinearLayoutManager;
import android.support.v7.widget.RecyclerView;
import android.view.LayoutInflater;
import android.view.View;
import android.view.ViewGroup;

import com.popokis.models.Equivalence;
import com.popokis.willyfog_mobile.content.EquivalenceContent;

public class EquivalenceFragment extends Fragment {

    public static String ARG_SUBJECT = "subject";
    public static String VALUE_SUBJECT;

    private int mColumnCount = 1;
    private OnListFragmentInteractionListener mListener;

    public EquivalenceFragment() {
    }

    @Override
    public void onCreate(Bundle savedInstanceState) {
        super.onCreate(savedInstanceState);

        if (getArguments() != null) {
            mColumnCount = getArguments().getInt(ARG_SUBJECT);
        }
    }

    @Override
    public View onCreateView(LayoutInflater inflater, ViewGroup container,
                             Bundle savedInstanceState) {
        View view = inflater.inflate(R.layout.fragment_equivalence_list, container, false);

        Bundle args = getArguments();
        VALUE_SUBJECT = args.getString(ARG_SUBJECT);

        // Set the adapter
        if (view instanceof RecyclerView) {
            Context context = view.getContext();
            RecyclerView recyclerView = (RecyclerView) view;
            if (mColumnCount <= 1) {
                recyclerView.setLayoutManager(new LinearLayoutManager(context));
            } else {
                recyclerView.setLayoutManager(new GridLayoutManager(context, mColumnCount));
            }

            // Init equivalence list
            EquivalenceContent e = new EquivalenceContent(new ProgressDialog(this.getActivity()));

            recyclerView.setAdapter(new MyEquivalenceRecyclerViewAdapter(e.getITEMS(), mListener));
        }
        return view;
    }

    @Override
    public void onAttach(Context context) {
        super.onAttach(context);
        if (context instanceof OnListFragmentInteractionListener) {
            mListener = (OnListFragmentInteractionListener) context;
        } else {
            throw new RuntimeException(context.toString()
                    + " must implement OnListFragmentInteractionListener");
        }
    }

    @Override
    public void onDetach() {
        super.onDetach();
        mListener = null;
    }

    public interface OnListFragmentInteractionListener {
        void onListFragmentInteraction(Equivalence item);
    }
}
